package Modal;

/**
 *
 * @author Admin
 */
public class CommentProduct {
     private int id ;
    private String name,pid, date,content;
    private int like;

    public CommentProduct() {
    }

    public CommentProduct( String name, String pid, String date, String content, int like) {
        this.id = id;
        this.name = name;
        this.pid = pid;
        this.date = date;
        this.content = content;
        this.like = like;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getLike() {
        return like;
    }

    public void setLike(int like) {
        this.like = like;
    }
    

}